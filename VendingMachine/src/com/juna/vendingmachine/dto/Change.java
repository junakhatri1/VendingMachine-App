package com.juna.vendingmachine.dto;

public class Change {
	private Integer cents;
	private Integer nickles;
	private Integer dimes;
	private Integer quarters;
	private Integer dollars;
	
	
	
	
	public Integer getCents() {
		return cents;
	}
	public void setCents(Integer cents) {
		this.cents = cents;
	}
	public Integer getNickles() {
		return nickles;
	}
	public void setNickles(Integer nickles) {
		this.nickles = nickles;
	}
	public Integer getDimes() {
		return dimes;
	}
	public void setDimes(Integer dimes) {
		this.dimes = dimes;
	}
	public Integer getQuarters() {
		return quarters;
	}
	public void setQuarters(Integer quarters) {
		this.quarters = quarters;
	}
	public Integer getDollars() {
		return dollars;
	}
	public void setDollars(Integer dollars) {
		this.dollars = dollars;
	}
	
	

	

	
	
	
	
	
	
}
